package com.zl.bean;

public class Message {
	private String msg;//提示信息
	private String url;//要返回的路径
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
